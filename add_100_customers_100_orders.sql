SELECT 'Total Revenue 2024', ROUND(SUM(total)::numeric, 2)::text FROM orders WHERE order_date >= '2024-01-01' AND order_date < '2025-01-01'
UNION ALL
SELECT 'Total Revenue 2025', ROUND(SUM(total)::numeric, 2)::text FROM orders WHERE order_date >= '2025-01-01';
